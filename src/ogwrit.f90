SUBROUTINE ogwrit(Lev,Str)
! ======================================================================
! 2014/07/29 | J. SCHOENMAEKERS | NEW
! ======================================================================
   IMPLICIT NONE
! ======================================================================
   INCLUDE "ogdata.inc"
! ======================================================================
   CHARACTER*(*) Str
   INTEGER(4) Lev
! ======================================================================
   IF ( Lev<=Loglev ) THEN
      WRITE (Loglun,'(A)') Str
      FLUSH (Loglun)
   ENDIF
! ======================================================================
END SUBROUTINE ogwrit
