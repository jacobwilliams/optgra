SUBROUTINE sum2v(V1,V2,V,K)
! ======================================================================
! V(1:K) = V1(1:K) + V2(1:K)
! ======================================================================
   IMPLICIT NONE
   INTEGER*4 i , K
   REAL*8 V , V1 , V2
! ======================================================================
   DIMENSION V1(*) , V2(*) , V(*)
! ======================================================================
   DO i = 1 , K
      V(i) = V1(i) + V2(i)
   ENDDO
! ======================================================================
END SUBROUTINE sum2v
