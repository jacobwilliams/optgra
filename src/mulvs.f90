SUBROUTINE mulvs(X,A,Z,Kd)
! ======================================================================
! VECTOR Z (1:KD) = VECTOR X (1:KD) * A
! ======================================================================
!I X   R8  *  VECTOR
!I A   R8     SCALAR
!I Z   R8  *  VECTOR
!I KD  I4     NUMBER OF ELEMENTS TO BE USED
! ======================================================================
   IMPLICIT NONE
   REAL*8 A , X , Z
   INTEGER*4 i , Kd
! ======================================================================
   DIMENSION X(*) , Z(*)
! ======================================================================
   DO i = 1 , Kd
      Z(i) = X(i)*A
   ENDDO
! ======================================================================
END SUBROUTINE mulvs
